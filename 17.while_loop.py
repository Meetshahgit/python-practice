# a structure in Python which allows you to loop through and run multiple line of code with one line of code
# In Python the code keeps running until the requirement is satisfied
i = 1
while i <= 10:
    print(i)
    i += 1

# this loops will keep adding 1 until we reach 10 and once we reach 10 the loop will break and move in the next line
print("Done with loop")

